def get_features(train_loader, model, args):
    """
    Switch to eval mode:
    Under the protocol of linear classification on frozen features/models,
    it is not legitimate to change any part of the pre-trained model.
    BatchNorm in train mode may revise running mean/std (even if it receives
    no gradient), which are part of the model parameters too.
    """
    model.eval()
    count = 0
    num_classes = args.num_classes
    class_vec = np.zeros((num_classes,128))
    class_count = np.zeros((num_classes))
    
    
    for i, (images, target) in enumerate(train_loader):
        # measure data loading time
        
        if args.gpu is not None:
            images = images.cuda(args.gpu, non_blocking=True)
        target = target.cuda(args.gpu, non_blocking=True)
        
        output = model.encoder_q(images)
        #output = torch.nn.functional.normalize(output, dim=1)
        output_np=output.detach().cpu().numpy()
        target_np = target.detach().cpu().numpy()
        del output
        del images
        del target
        for j in range(num_classes):
            #print(target_np)
            index_select = (target_np == j)
            feature_select = output_np[index_select,:]
            #print(np.sum(index_select))
            #print(feature_select.shape)
            class_vec[j,:] += np.sum(feature_select,axis=0,keepdims=False)
            class_count[j] += np.sum(index_select)
        print('batch '+str(i))
    pickle.dump(class_vec,open('class_mean/'+args.dataset_name+'/class_vec_unnorm.pkl','wb'))
    pickle.dump(class_count,open('class_mean/'+args.dataset_name+'/class_count_unnorm.pkl','wb'))
    

    return class_vec, class_vec


